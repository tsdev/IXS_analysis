wG=v(6);
y=y0+A*(mu*(2./pi)*(wL*1./(4.*(x - xc).^2 + wL^2))+(1-mu)*(sqrt(4.*log(2.))/(sqrt(pi)*wG))*exp(-(4*log(2.)/wG^2)*(x - xc).^2 ));
